#include<asm/io.h>
#include<hardware/pic.h>

void pic_remap(){

	outb(0x20, 0x11);
    outb(0xA0, 0x11);

    outb(0x21, 0x20);
    outb(0xA1, 0x28);

    outb(0x21, 0x04);
    outb(0xA1, 0x02);

    outb(0x21, 0x01);
    outb(0xA1, 0x01);

    outb(0x21, 0x0);
    outb(0xA1, 0x0);
}

void pic_sendEOI(uint8_t irq){
	if(irq >= 8)
		outb(0xA0,0x20);
 
	outb(0x20,0x20);
}